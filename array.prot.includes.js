// includes function
// returns a true if array contains the value
let numbers = [1,2,3,4,5,6];
let numbers22 = [1,2,3,4,5,6,NaN];


console.log(numbers.includes(4));  // returns true
console.log(numbers.includes(45));   // returns false


console.log(numbers22.includes(NaN));  // returns true




